# >  Maior que
# <  Menor que
# >= Maior ou Igual a
# <= Menor ou Igual a
# == Igual a
# != Diferente a
def main():
    
    primeiro_numero = 5
    segundo_numero  = 5

    if primeiro_numero == segundo_numero:
        print("Os numeros são iguais")
    elif primeiro_numero > segundo_numero:
        print("O Primeiro numero é maior")
    else:
        print("O Segundo numero é maior")
        
main()